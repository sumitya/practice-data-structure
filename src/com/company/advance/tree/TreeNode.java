package com.company.advance.tree;

class TreeNode {
    int val;
    String str;
    TreeNode left;
    TreeNode right;

    TreeNode(int x) {
        val = x;
        left = null;
        right = null;
    }
    TreeNode(String x) {
        str = x;
        left = null;
        right = null;
    }
}
